#include "floah-data/bool_data_source.h"

namespace floah
{
    ////////////////////////////////////////////////////////////////
    // Constructors.
    ////////////////////////////////////////////////////////////////

    BoolDataSource::BoolDataSource(bool& v) : value(&v) {}

    BoolDataSource::~BoolDataSource() noexcept = default;

    ////////////////////////////////////////////////////////////////
    // Getters.
    ////////////////////////////////////////////////////////////////

    bool BoolDataSource::get() { return *value; }

    ////////////////////////////////////////////////////////////////
    // Setters.
    ////////////////////////////////////////////////////////////////

    void BoolDataSource::set(const bool v) { *value = v; }

}  // namespace floah
